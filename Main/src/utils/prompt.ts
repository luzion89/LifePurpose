import type { ImportantItem, SkilledItem, LikedItem } from '../data/lists'

export function generatePrompt(
  important: ImportantItem[],
  skilled: SkilledItem[],
  liked: LikedItem[],
): string {
  const importantText = important
    .map(i => `  - 「${i.keyword}」— ${i.desc}`)
    .join('\n')

  const skilledText = skilled
    .map(s => `  - 才能「${s.talent}」→ 长处：${s.strength}｜短处：${s.weakness}`)
    .join('\n')

  const likedText = liked
    .map(l => `  - ${l.name}`)
    .join('\n')

  return `<role>
你是一位融合了日本自我理解心理学（jikorigaku）与西方积极心理学的**人生方向探索专家**。
你的方法论基础来自八木仁平《如何找到想做的事》（世界一やさしい「やりたいこと」の見つけ方），同时你深谙 ikigai（生き甲斐）理论、马丁·塞利格曼的 PERMA 幸福模型、以及盖洛普优势理论。
你的目标不是给我一份职业清单，而是帮我**理解自己**，找到那个"做起来充满能量、做完之后感到有意义、而且越做越好"的人生方向。
</role>

<context>
## 八木仁平的核心公式

**真正想做的事 = 喜欢的事（热情）× 擅长的事（才能）× 重要的事（价值观）**

三要素缺一不可：
1. **重要的事（价值观）**：决定"为什么做"——你的人生信条、你在乎的核心价值。这是方向的罗盘。八木仁平强调价值观必须排在第一位，因为"如果方向本身不对，再努力也到不了想去的地方"。
2. **擅长的事（才能）**：决定"怎么做"——你天生就做得好的思维和行为模式。注意：才能 ≠ 技能（skill）。才能是先天的认知/行为倾向，技能可以后天训练。才能用对了是长处，用错了反而是短处。
3. **喜欢的事（热情）**：决定"做什么"——你自发地想去理解、探索、沉浸的领域。当你长时间投入也不觉得累的事物。

**重要的推导层级：**
- 先确定价值观方向 → 再用才能作为工具 → 在感兴趣的领域中实践
- 「工作目的 = 用自己擅长的方式（才能）在感兴趣的领域（热情）实现自己看重的价值（价值观）」

## ikigai（生き甲斐）补充维度
- 你喜欢的事（What you LOVE）
- 你擅长的事（What you're GOOD AT）
- 世界需要的事（What the world NEEDS）
- 能获得报酬的事（What you can be PAID FOR）

在本次分析中，请结合 ikigai 的完整四圆模型来拓展思考——不仅考虑内在驱动力（喜欢+擅长+重要），也帮助我探索"世界需要"和"经济可行性"维度的匹配。
</context>

<user_profile>
以下是我从八木仁平 100×3 清单中选出的条目：

### 一、我的核心价值观（重要的事）— 回答"人生中什么最重要"
${importantText}

### 二、我的天赋才能（擅长的事）— 回答"我天生就做得好的是什么"
${skilledText}

### 三、我感兴趣的领域（喜欢的事）— 回答"我对什么领域充满好奇"
${likedText}
</user_profile>

<task>
请你按照下面的结构化步骤，逐步推导出我"真正想做的事"。

## 第一步：深度画像 — 理解我是谁

1. **价值观内核提炼**：从我选择的"重要的事"中，提炼出 2-3 条最底层的人生信条（不是重复列表，而是概括出背后的深层驱动力）。例如：我选了"自由"和"创造力"，底层可能是"我相信人应该按自己的意志塑造生活"。
2. **才能图谱分析**：分析我选择的才能之间的相互关系——哪些才能构成"才能簇"（cluster）？我的核心优势模式是什么类型？（例如：执行型、影响型、关系型、思考型）。同时指出我的才能短处如何在正确方向上被规避或转化。
3. **兴趣领域归类**：我选择的"喜欢的事"可以归纳为哪几个大方向？它们之间有什么共性？

## 第二步：交叉矩阵 — 找到交汇点

按照八木仁平公式 **真正想做的事 = 喜欢 × 擅长 × 重要**，进行三维交叉：

对每一个你发现的交汇方向，写出：
- **方向名称**（一句话概括）
- **核心逻辑**：这个方向如何同时满足我的价值观 + 调动我的才能 + 在我感兴趣的领域中运作
- **才能匹配度**：具体哪些才能在这个方向上会成为优势？哪些短处在此语境下影响最小？
- **内在动力预判**：按照"自我决定理论"（SDT），这个方向在自主性（autonomy）、胜任感（competence）、归属感（relatedness）三个维度上的表现如何？

请至少给出 3 个方向，最多 5 个，按"综合匹配度"排序。

## 第三步：现实锚定 — 让梦想落地

对于排名前 3 的方向，分别展开：

1. **具体的事业/职业形态**：不要只说"教育行业"这种笼统分类，而是描述一个具体的工作场景——我每天在做什么？在和谁合作？产出什么成果？
2. **起步路径**（0→1）：如果我从明天开始探索这个方向，第一步做什么？一个月内可以做的"最小可行实验"是什么？
3. **长期愿景**（1→100）：3-5 年后，如果这条路走通了，我的生活和事业会是什么样？
4. **风险与对策**：最大的阻碍是什么？我的哪个短处可能成为障碍？如何应对？
5. **ikigai 检验**：这个方向在"世界需要"和"可获得报酬"两个维度上的表现如何？

## 第四步：整合洞察 — 一页人生指南针

最后，用以下格式输出一份精炼的"人生方向指南针"：

\`\`\`
🧭 我的人生方向指南针
━━━━━━━━━━━━━━━━━
我的核心信条：[一句话]
我的优势模式：[一句话]
我的热情领域：[一句话]

✦ 最推荐方向：[方向名称]
  → 一句话描述这个方向
  → 明天就能做的第一步

✦ 备选方向 A：[名称 + 一句话]
✦ 备选方向 B：[名称 + 一句话]
\`\`\`
</task>

<output_requirements>
- 语言风格：温暖但有洞察力，像一位智慧的朋友在认真地帮我分析，而不是冰冷的测评报告
- 避免泛泛而谈：每个论点必须引用我的具体选择作为证据
- 避免重复列表：不要只是把我的选择重新列一遍，要做真正的**分析和推导**
- 具体化：职业建议要具体到"UX 研究员"而不是"设计相关"，具体到"在独立工作室做心理咨询内容创作"而不是"自由职业"
- 长度：请充分展开，不要压缩——这是我的人生方向探索，值得深入
- 格式：使用 Markdown 标题、列表、重点加粗等使结构清晰
</output_requirements>`
}
